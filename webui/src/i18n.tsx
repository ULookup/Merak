const dictionaries: Record<Locale, Dictionary> = { zh, en };

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  toggleLocale: () => void;
  t: (key: string) => string;
}

const defaultValue: I18nContextValue = {
  locale: 'en',
  setLocale: () => {},
  toggleLocale: () => {},
  t: (key) => dictionaries.en[key] ?? key,
};

const I18nContext = createContext<I18nContextValue>(defaultValue);

function initialLocale(): Locale {
  if (typeof window === 'undefined') return 'zh';
  return window.localStorage.getItem('merak.locale') === 'en' ? 'en' : 'zh';
}

export function I18nProvider({
  children,
  defaultLocale,
}: {
  children: ReactNode;
  defaultLocale?: Locale;
}) {
  const [locale, setLocaleState] = useState<Locale>(() => defaultLocale ?? initialLocale());

  const value = useMemo<I18nContextValue>(() => {
    function setLocale(next: Locale) {
      setLocaleState(next);
      window.localStorage.setItem('merak.locale', next);
    }

    return {
      locale,
      setLocale,
      toggleLocale: () => setLocale(locale === 'zh' ? 'en' : 'zh'),
      t: (key) => dictionaries[locale][key] ?? dictionaries.zh[key] ?? key,
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}

export function LanguageToggle() {
  const { locale, toggleLocale } = useI18n();
  return (
    <button type="button" onClick={toggleLocale} aria-label="Switch language" title="Switch language">
      <Languages size={16} aria-hidden="true" strokeWidth={2.2} />
      <span>{locale === 'zh' ? 'EN' : '中'}</span>
    </button>
  );
}
